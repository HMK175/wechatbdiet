function buildQuotaMap(raw) {
  const map = new Map();
  String(raw || "")
    .split("\n")
    .map((l) => l.trim())
    .filter((l) => l && !l.startsWith("#"))
    .forEach((line) => {
      const [wStr, hStr, valRaw] = line.split(",").map((s) => (s || "").trim());
      if (!wStr || !hStr || !valRaw) return;
      const value = valRaw.replace("4.8/38/2.1", "4.8/3.8/2.1").trim();
      if (value.includes("#VALUE")) return;
      const parts = value.split("/").map((x) => parseFloat(x));
      if (parts.length !== 3 || parts.some((x) => !Number.isFinite(x))) return;
      const key = `${parseInt(wStr, 10)}|${parseInt(hStr, 10)}`;
      map.set(key, {
        carbsTrainingPerKg: parts[0],
        carbsRestPerKg: parts[1],
        proteinPerKg: parts[2],
      });
    });
  return map;
}

const QUOTA_MAP = {
  male: { bulk: buildQuotaMap(QUOTA_RAW.male.bulk), cut: buildQuotaMap(QUOTA_RAW.male.cut) },
  female: { bulk: buildQuotaMap(QUOTA_RAW.female.bulk), cut: buildQuotaMap(QUOTA_RAW.female.cut) },
};

function getQuotaExact(gender, phase, weightKg, heightCm) {
  const map = QUOTA_MAP[gender]?.[phase];
  if (!map) return null;
  return map.get(`${weightKg}|${heightCm}`) || null;
}

function nearestAvailableQuota(gender, phase, heightCm, weightKg) {
  const dims = QUOTA_DIMENSIONS[gender]?.[phase];
  if (!dims) return null;
  const map = QUOTA_MAP[gender]?.[phase];
  if (!map) return null;

  const minH = dims.heights[0];
  const maxH = dims.heights[dims.heights.length - 1];
  const minW = dims.weights[0];
  const maxW = dims.weights[dims.weights.length - 1];
  const h0 = clamp(Math.round(heightCm / 5) * 5, minH, maxH);
  const w0 = clamp(Math.round(weightKg / 5) * 5, minW, maxW);

  let best = null;
  let bestDist = Infinity;
  dims.weights.forEach((w) => {
    dims.heights.forEach((h) => {
      const key = `${w}|${h}`;
      const v = map.get(key);
      if (!v) return;
      const dist = Math.abs(w - w0) + Math.abs(h - h0);
      if (dist < bestDist) {
        bestDist = dist;
        best = { heightCm: h, weightKg: w, ...v };
      }
    });
  });
  return best;
}

function suggestMacrosFromQuotaTable({ gender, phase, heightCm, weightKg }) {
  return nearestAvailableQuota(gender, phase, heightCm, weightKg);
}

